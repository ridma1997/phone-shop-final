    private void searchTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTxtKeyReleased
        String val = searchTxt.getText().trim();
        if (!val.isEmpty()) {
            String sql = "select g.grn_id as id,g.enter_date as date, s.sup_id as sup, s.company_name as supName,g.total_amount as amount,g.total_discount as discount, g.status as status from grn g inner join supplier s on s.sup_id = g.sup_id where g.status != '" + model.GRN.DEACTIVATE + "'";
            String type = "";

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            if (grnIdRadioButton.isSelected()) {
                type = "g.grn_id";
                sql += " and " + type + " like '%" + val + "%'";
            } else if (supIdRadioButton.isSelected()) {
                type = "g.sup_id";
                sql += " and " + type + " like '%" + val + "%'";
            } else if (dateRadioButton.isSelected()) {
                sql += " and g.enter_date like '" + val + "'";
            }
            try {
                ResultSet rs = Database.search(sql);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString("id"));
                    v.add(rs.getString("date"));
                    v.add(rs.getString("sup"));
                    v.add(rs.getString("supName"));
                    double ttl = Double.parseDouble(rs.getString("amount"));
                    double dis = Double.parseDouble(rs.getString("discount"));
                    v.add(ttl - dis + "");
                    String status = "PENDING";
                    if (rs.getString("status").equals(model.GRN.COMPLETE)) {
                        status = "COMPLETED";
                    }
                    v.add(status);
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            setDataToTable();
        }
    }//GEN-LAST:event_searchTxtKeyReleased

