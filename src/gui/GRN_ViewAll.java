    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        DefaultTableModel dtm2 = (DefaultTableModel) jTable2.getModel();
        try {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow >= 0) {
                String grnID = dtm.getValueAt(selectedRow, 0).toString();
                String status = dtm.getValueAt(selectedRow, 5).toString();
                int check = JOptionPane.showConfirmDialog(this, "Do you really want to delete this GRN", "Warning", JOptionPane.YES_NO_OPTION);
                if (check == JOptionPane.YES_OPTION) {
                    if (!status.equals("COMPLETED")) {
                        int i = Database.iud("update grn set status='" + model.GRN.DEACTIVATE + "' where grn_id='" + grnID + "'");
                        if (i == 1) {
                            dtm2.setRowCount(0);
                            setDataToTable();
                            JOptionPane.showMessageDialog(this, "GRN Deleted Succussfully..", "ALERT", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(this, "Delete Faliuer", "ERROR", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "This GRN is already approved..You cant delete it", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please select a row from above Table", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Please select a row from above Table", "ERROR", JOptionPane.ERROR_MESSAGE);
        } catch (ArrayIndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(this, "Please select a row from above Table", "ERROR", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

