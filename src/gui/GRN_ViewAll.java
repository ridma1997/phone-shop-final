    private void setDataToGrnStockTbl() {
        DefaultTableModel dtm1 = (DefaultTableModel) jTable1.getModel();
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        try {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow >= 0) {
                String grnId = dtm1.getValueAt(selectedRow, 0).toString();
                String sql = "select gs.product_pro_id as proId ,p.name as proName, gs.qty as qty, gs.get_unit_price as price, gs.unit_discount as discount from grn_stock gs inner join product p on p.pro_id = gs.product_pro_id where gs.grn_id = '" + grnId + "' and gs.status !='" + GRN_Stock.OUT_OF_STOCK + "'";
                ResultSet rs = Database.search(sql);
                dtm.setRowCount(0);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString("proID"));
                    v.add(rs.getString("proName"));
                    v.add(rs.getString("qty"));
                    v.add(rs.getString("price"));
                    v.add(rs.getString("discount"));
                    double ttl = (Double.parseDouble(rs.getString("qty")) * Double.parseDouble(rs.getString("price"))) - Double.parseDouble(rs.getString("qty")) * Double.parseDouble(rs.getString("discount"));
                    v.add(ttl);
                    dtm.addRow(v);
                }
                grnIDLbl.setText(grnId);
                netTotLabel.setText(dtm1.getValueAt(selectedRow, 4).toString());
                double total = 0;
                double discount = 0;
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    discount += Double.parseDouble(dtm.getValueAt(i, 4).toString()) * Double.parseDouble(dtm.getValueAt(i, 2).toString());
                    total += Double.parseDouble(dtm.getValueAt(i, 3).toString()) * Double.parseDouble(dtm.getValueAt(i, 2).toString());
                }
                totDiscountLabel.setText("" + discount);
                totalAmntLabel.setText("" + total);
            } else {

            }
        } catch (NullPointerException ex) {

        } catch (ArrayIndexOutOfBoundsException ex) {

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
